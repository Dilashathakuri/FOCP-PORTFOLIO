

#  comma as the separator
print("Apple", "Banana", "Cherry", sep=", ")

#  dash as the separator
print("2025", "01", "07", sep="-")

#  newline as the separator
print("Line 1", "Line 2", "Line 3", sep="\n")

#  tab as the separator
print("Column1", "Column2", "Column3", sep="\t")

#  no separator 
print("Hello", "World", "!", sep="")

# custom string as the separator
print("A", "B", "C", sep=" ---> ")

# Testing with numerical values
print(1, 2, 3, 4, sep=" | ")
